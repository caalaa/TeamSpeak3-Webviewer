// Returns the current position on the viewport of an object
function getDialogPosition(obj)
{
    var pos = {
        x: 0,
        y: 0
    };
    
    pos.x = jQuery(obj).offset().left - jQuery("body").scrollLeft();
    pos.y = jQuery(obj).offset().top - jQuery("body").scrollTop();
    
    return pos;
}
